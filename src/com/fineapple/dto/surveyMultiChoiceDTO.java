package com.fineapple.dto;

public class surveyMultiChoiceDTO {
	
	private String seq;
	private String answer;
	private String etc;
	private String serveyAnswerSeq;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	public String getEtc() {
		return etc;
	}
	public void setEtc(String etc) {
		this.etc = etc;
	}
	public String getServeyAnswerSeq() {
		return serveyAnswerSeq;
	}
	public void setServeyAnswerSeq(String serveyAnswerSeq) {
		this.serveyAnswerSeq = serveyAnswerSeq;
	}
}
